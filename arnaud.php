					<h2>Le culte des ancêtres</h2>
					<p>En Afrique, le culte des ancêtres, est une pratique sociale fortement implantée. Elle peut varier selon les régions et les ethnies.</p>
					<p>En Afrique comme au Cameroun, les Anciens ont une place très importante dans la société et la famille. Ils sont les guides et les garants de la transmission des valeurs au travers des générations. Tous les membres de la famille se réfèrent donc au grand-père, lorsqu’il y a un problème, pour demander conseil ou encore pour obtenir sa bienveillance, et ceci même après sa mort. </p>
					<p>En effet, chez les Bamilékés (peuple du Cameroun), l’être humain est considéré comme étant double dans sa constitution. D’un côté, il y a le corps physique visible et de l’autre, il y a l’âme spirituelle invisible. À la mort, l’âme se sépare du corps et s’en va dans l’autre monde rejoindre les siens, qui l’y ont précédé. Pour les Bamilékés, les ancêtres ne meurent pas réellement, car ils continuent à s’intéresser à la vie de leurs descendants. Il est donc possible de les contacter pour qu’ils intercèdent auprès des Esprits divins universels en faveur de leurs proches. </p>
					<p>Lorsque le grand-père décède, un successeur est désigné par la famille. Ce dernier reprend le flambeau et accueillera le corps du défunt sur son terrain. Par la suite, le successeur construira son foyer à cet endroit. Deux ou trois ans après le décès, le crâne du défunt est prélevé et déposé dans un endroit de la maison pour que la famille puisse continuer à communiquer avec lui. Dès lors, chaque fois qu’ils veulent parler à leur ancêtre, ils se recueillent devant le crâne du défunt et lui font une offrande (sel, bière, etc.).</p>
					<p>Le culte des crânes tient une place importante dans les traditions des Camerounais Bamiléké, mais reste souvent incompris par les autres cultures, notamment occidentales. Pour comprendre cette tradition, il faut se pencher sur ses fondements. Dans la spiritualité Bamiléké, tout objet matériel qui a appartenu pendant longtemps au défunt est fortement imprégné de son essence spirituelle propre et peut donc constituer un excellent moyen pour faciliter le contact avec son âme dans l’au-delà. La tête est traditionnellement associée à l’esprit, à la pensée, à la conscience et à l’âme. C’est donc la raison pour laquelle, l’os crânien est choisi.</p>
