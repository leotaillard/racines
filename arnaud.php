					<h2>Chiffre clé</h2>
				<p><b>280.000 à 500.000 tonnes,</b> c’est la production annuelle de bananes du Cameroun.</p>
				<img src="img/arnaud/banane.png" alt="" />
				<p>La banane plantain est plus qu’un simple fruit. C’est le pilier de la sécurité alimentaire et de l’économie générale du Cameroun. En effet, la récolte des bananes dure toute l’année puisqu’elle ne peut se récolter en une fois. Cela garantit donc un revenu stable et intéressant pour les agriculteurs camerounais.</p>
				<p>La banane est aussi le moteur des améliorations sociales et environnementales des filières agricoles. Le salaire minimum étant payé 50 % supérieur au salaire minimum garanti. </p>
				
