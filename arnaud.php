					<h2>Les Camerounais et le football, toute une histoire</h2>
					<p>À l’annonce d’un match de l’équipe nationale, le temps s’arrête. Tout le monde se réunit autour du téléviseur du coin pour suivre attentivement leurs « Lions Indomptables ».</p>
					<p>Comme bien d’autres pays d’Afrique, le football occupe une place très importante au Cameroun, même si les infrastructures sportives restent limitées et anciennes. Beaucoup datent de 1972, année où le pays a été honoré d’être l’organisateur de la Coupe d’Afrique.</p>
					<p>Alors quand leurs « Lions Indomptables » gagnent, c’est tout un pays qui gagne. Le palmarès de l’équipe du Cameroun est d’ailleurs brillant : quatre fois vainqueurs de la CAN (Coupe d’Afrique des Nations), une fois finaliste de la Coupe Intercontinentale en 2003 en France et même championne olympique en 2000 à Sydney. </p>
					<p>Quand vous croisez une Camerounaise arborant un maillot de l’équipe nationale de son pays, sachez que c’est bien plus qu’un simple habit, c’est le symbole d’une nation, de ses valeurs et de la paix d’un peuple.</p>
					<quote>« T’es camerounais, t’as pas de maillot… T’es pas camerounais. Au Cameroun, le foot c’est une religion. On est né avec, on grandit avec, on meurt avec. »</quote>
