					<h2>Compagnon en « tour des pays »</h2>
					<p>En Europe germanophone, les métiers traditionnels du bâtiment, les charpentiers et tailleurs de pierre, ainsi que de nombreux métiers manuels (suivant les différentes sociétés) ont conservé la tradition du « tour des pays » <i>(Wanderschaft).</i> Dans le bas moyen âge, le « tour des pays » était obligatoire si un compagnon voulait faire le brevet professionnel. Le but était d’apprendre des nouvelles pratiques de travail et de faire l’expérience des endroits, des régions et des pays étrangers. Un ouvrier en tour étant appelé <i>Fremder.</i></p>
					<p>Les compagnons portent un costume traditionnel en velours noir (ou gris, blanc, beige... suivant les métiers) à pattes d'éléphant, un grand chapeau et un bâton, l'ensemble étant appelé <i>Kluft.</i></p>
					
					<p>Actuellement, plusieurs sociétés de compagnonnage existent encore. Elles sont toutes réunies sous l’organisation faîtière <a href="http://cceg.eu" target="_blank">C.C.E.G. (Confederation Compagnonnages Européens)</a>. Ces sociétés se distinguent par certaines marques distinctives de la tenue (cravate grise, sans cravate, etc.). Les compagnons partent pour deux/trois<sup>*</sup> ans, sans pouvoir s'approcher à plus de 50/60<sup>*</sup> km de leur famille et doivent changer de région ou de pays, tous les trois mois.</p>
					<p>Dans son livret de voyage <i>(Wanderbuch)</i> « l’étranger » cueille les sceaux/tampons des villages visités et les certificats de travail de ses employeurs.</p>
					<p><sup>*</sup> Les règles sont différentes d’une société à l’autre.</p>
				
				<p>En savoir plus :<a href="http://fr.wikipedia.org/wiki/Tour_de_France_du_compagnonnage" target="_blank">Article wikipédia</a></p>
